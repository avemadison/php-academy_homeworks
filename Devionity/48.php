<!--Создать алгоритм определения всех простых чисел в промежутке от 1 до 100 при помощи while. Простое число - это число которое делится только на себя и на 1-->
<?php
$i = 1;
while ($i <= 100) {
    echo $i . '<br>';
    $i++;
}
?>